public class CircleEquation {
    private double x0, y0, R;

    public CircleEquation(double xo, double yo, double r) {
        this.x0 = xo;
        this.y0 = yo;
        R = r;
    }

    public double getX0() {
        return x0;
    }

    public double getY0() {
        return y0;
    }

    public double getR() {
        return R;
    }
}
